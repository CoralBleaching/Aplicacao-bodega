    private void botaoVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoVenderActionPerformed
        // TODO add your handling code here:
        Produto produto;
        if (focoEmEstoque) {
            produto = (Produto) listaEstoque.getSelectedValue();
        } else {
            produto = (Produto) listaFalta.getSelectedValue();
        }
        if (produto != null) {
            int n = Integer.valueOf(textoVender.getText());
            try {
                bodega.venderProduto(produto.getCodigo(), n);
                GerenciadorDeArquivo.salvarBodega(arquivo, bodega);
                painelCadastro.atualizarListas();
            } catch (PNEexception | QIexception e) {
                System.out.println(e.getMessage());
                ExceptionWindow aviso = new ExceptionWindow(e, this, true);
                aviso.setVisible(true);
            }
        }
    }//GEN-LAST:event_botaoVenderActionPerformed

