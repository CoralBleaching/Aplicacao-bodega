    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = textoBusca.getText();
        try {
            Produto produto = bodega.consultarProduto(codigo);
            if (produto.getQuantidade() > 0) {
                listaEstoque.setSelectedValue(produto, true);
                listaEstoque.requestFocusInWindow();
            } else {
                listaFalta.setSelectedValue(produto, true);
                listaFalta.requestFocusInWindow();
            }
            jTabbedPane1.setSelectedIndex(0);
        } catch (PNEexception e) {
            ExceptionWindow aviso = new ExceptionWindow(e, null, true);
            aviso.setVisible(true);
        }
    }//GEN-LAST:event_botaoBuscarActionPerformed
    
