    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        // TODO add your handling code here:
        Produto produto;
        if (focoEmEstoque) {
            produto = (Produto) listaEstoque.getSelectedValue();
        } else {
            produto = (Produto) listaFalta.getSelectedValue();
        }
        if (produto != null) {
            try {
                bodega.removerProduto(produto.getCodigo());
                GerenciadorDeArquivo.salvarBodega(arquivo, bodega);
                painelCadastro.atualizarListas();
            } catch (PNEexception e) {
                System.out.println(e.getMessage());
                ExceptionWindow aviso = new ExceptionWindow(e, null, true);
                aviso.setVisible(true);
            }
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

